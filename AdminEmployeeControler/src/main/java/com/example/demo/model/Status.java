package com.example.demo.model;

public class Status {

	private String message;

	public Status(String message) {
		super();
		this.message = message;
	}

	public Status() {
		super();
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
}
